package chess;

public class ChessPosition {
    int x = 0;
    int y = 0;
    int chessId = 0;
    int chessVal = 0;

    public void setX(int x) {
        this.x = x;
    }

    public int getX() {
        return this.x;
    }

    public void setY(int y) {
        this.y = y;
    }

    public int getY() {
        return this.y;
    }

    public void setChessId(int chessId) {
        this.chessId = chessId;
    }

    public int getChessId() {
        return this.chessId;
    }

    public void setChessVal(int chessVal) {
        this.chessVal = chessVal;
    }

    public int getChessVal() {
        return this.chessVal;
    }
}
